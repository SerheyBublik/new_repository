def upper_(str_):
    """
    Принимает на вход строку и возвращает ее со всеми заглавными буквами.
    """
    return str_.upper()


def title_(str_):
    """
     Dелает заглавными первые буквы каждого слова в строке, поступившей на вход функции.
    :param str_:
    :return:
    """
    return str_.title()
